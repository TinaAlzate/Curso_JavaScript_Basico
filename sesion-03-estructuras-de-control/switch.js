//Sentencias switch
let nota = 8;

switch (nota){
    case 5:
        console.log("Ecxelente");
        break;
    case 4:
        console.log("Muy bien");
        break;
    case 3:
        console.log("Bien");
        break;
    case 2:
        console.log("Insuficiente");
        break;
    case 1:
        console.log("Fatal");
        break;
    default:
        console.log("Error");
        break;
}